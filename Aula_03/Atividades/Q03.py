numero = int(input("Digite um número inteiro: "))

resultado = numero + 1357
print("O resultado do número {} + 1357 é: {}".format(numero, resultado))

resultado1 = resultado * 8
print("O resultado da multiplicação de {} * 8 é: {}".format(resultado, resultado1))

resultado2 = resultado1 / 5
print("O resultado da divisão de {} / 5 é: {}".format(resultado1, resultado2))

resultado3 = resultado2 ** 2
print("O resultado do elevado ao quadrado de {} é: {}".format(resultado2, resultado3))
